package one.jodi.odi12.flow;

public class FlowStrategyException extends RuntimeException {

    /**
     *
     */
    private static final long serialVersionUID = 1L;

    public FlowStrategyException() {
        super();
    }

    public FlowStrategyException(String message, Throwable cause) {
        super(message, cause);
    }

    public FlowStrategyException(String message) {
        super(message);
    }

    public FlowStrategyException(Throwable cause) {
        super(cause);
    }
}