package one.jodi.odi12.service;

public class Odi12RepositoryException extends RuntimeException {

    private static final long serialVersionUID = 1L;

    public Odi12RepositoryException() {
        super();
    }

    public Odi12RepositoryException(String message, Throwable cause) {
        super(message, cause);
    }

    public Odi12RepositoryException(String message) {
        super(message);
    }

    public Odi12RepositoryException(Throwable cause) {
        super(cause);
    }
}